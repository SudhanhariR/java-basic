package javaprogram;

public class operator {

	public static void main(String[] args) {
		int a=55;
		int b=35;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a*b);
		System.out.println(a%b);
		int x=3;
		x++;
		System.out.println(x);
		int y=3;
		y--;
		System.out.println(y);
		int c=45;
		c+=55;
		System.out.println(c);
		int e=55;
		int f=45;
		System.out.println(e==f);
		System.out.println(e!=f);
		System.out.println(e<f);
		System.out.println(e>f);
		System.out.println(e<=f);
		System.out.println(e>=f);
		// logical operator
		System.out.println((e==f)&&(e<f));
		System.out.println((e==f)||(e<f));
		System.out.println((!(e==f))||(e<f));
		
		
		
		
		
		
		
		 

	}

}
