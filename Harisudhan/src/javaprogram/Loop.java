package javaprogram;

public class Loop {

	public static void main(String[] args) {
		//Entry level check loop
		int i=0;
		while(i<=15) {
			System.out.println(i);
		i++;
		}
		//Do loop 
		int j=16;
		do {
			System.out.println(j);
			j++;
		}while(j<=15);
		
	
	    // for loop
	   for(int a=0; a<=15; a++)
		   System.out.println(a);
		   }

	}


